interface PaVerifyOptions {
  allCCP?: boolean;
  amount?: string;
  dueDate?: string;
  companyName?: string;
}

interface PaVerifyResult {
  xml: string;
  options: Required<PaVerifyOptions>;
}

const buildPaVerify = (opts: PaVerifyOptions = {}): PaVerifyResult => {
  const options: Required<PaVerifyOptions> = {
    allCCP: opts.allCCP ?? true,
    amount: opts.amount ?? '120.00',
    dueDate: opts.dueDate ?? '2030-07-31',
    companyName: opts.companyName ?? 'companyName',
  };

  return {
    options,
    xml: `<soapenv:Envelope xmlns:soapenv="http://schemas.xmlsoap.org/soap/envelope/"
    xmlns:paf="http://pagopa-api.pagopa.gov.it/pa/paForNode.xsd">
    <soapenv:Header />
    <soapenv:Body>
        <paf:paVerifyPaymentNoticeRes>
            <outcome>OK</outcome>
            <paymentList>
                <paymentOptionDescription>
                    <amount>${options.amount}</amount>
                    <options>EQ</options>
                    <dueDate>${options.dueDate}</dueDate>
                    <detailDescription>pagamentoTest</detailDescription>
                    <allCCP>${options.allCCP}</allCCP>
                </paymentOptionDescription>
            </paymentList>
            <paymentDescription>Pagamento di Test</paymentDescription>
            <fiscalCodePA>77777777777</fiscalCodePA>
            <companyName>${options.companyName}</companyName>
            <officeName>officeName</officeName>
        </paf:paVerifyPaymentNoticeRes>
    </soapenv:Body>
</soapenv:Envelope>`,
  };
};

